<p>
So, you want to build an OpenEEG and you are wondering where to begin. First you will need to acquaint yourself with the resources that are available to you:
</p>

<h2><a name="#mail">Mailing lists</a></h2>

<p>Register yourself on the OpenEEG mailing lists:
<a href="http://lists.sourceforge.net/lists/listinfo/openeeg-list"> Openeeg-list -- General discussion of hardware and software, for developers and users</a>
</p>

<p>
...and have a good browse of the mailing list archives:
<a href="http://sourceforge.net/mail/?group_id=35817">OpenEEG list archives</a>
</p>

<h2><a name="readme">Readme's</a></h2>

<p>Read the files in the OpenEEG documents directory:</p>
<ul>
	<li>All purpose guide: <a href="http://openeeg.sourceforge.net/doc/doc_skeleton.html">http://openeeg.sourceforge.net/doc/doc_skeleton.html</a><br></li>
	<li>Build tips: <a href="http://openeeg.sourceforge.net/doc/modularEEG-build-tips.txt">http://openeeg.sourceforge.net/doc/modularEEG-build-tips.txt</a><br></li>
	<li>Doc directory: <a href="http://openeeg.sourceforge.net/doc/">http://openeeg.sourceforge.net/doc/</a></li>
</ul>

<p>I also found these links useful:</p>
<ul>
	<li><a href="http://www.brainmaster.com/generalinfo/generalinfo.html">Brainmaster general information page</a> - General EEG information</li>
	<li><a href="http://www.amazon.com/exec/obidos/tg/detail/-/0874778506/">The high performance mind</a> - A book available from Amazon.com</li>
