</ul>

<h2><a name="plans">Plans</a></h2>
<p>
Download a copy of the latest schematics (<strong>modularEEG_vx.xx.zip</strong>) from:
<a href="http://sourceforge.net/project/showfiles.php?group_id=35817">OpenEEG project downloads page</a>
</p>

<p>This file contains:</p>
<ul>
	<li>schematics for the circuit boards</li>
	<li>a schematic for the cables (pic programmer and serial)</li>
	<li>flash firmware for the programmable controller chip</li>
	<li>parts summary and ordering information</li>
	<li>PCB templates for board manufacturers</li>
	<li>licensing information and release notes</li>
</ul>
<p>
The schematics are in Eagle cad format (available as freeware from <a href="http://www.cadsoftusa.com">http://www.cadsoftusa.com</a>).</p>
<p>
Alternatively PDF versions may be available at: <a href="http://openeeg.sourceforge.net/modeeg/pdf/">http://openeeg.sourceforge.net/modeeg/pdf/</a>
but may not be up to date.
</p>


<h2><a name="tools">Tools</a></h2>

<p>You will need the following tools:</p>
<ul>
	<li>Soldering iron with a fine tip</li>
	<li>Small wire cutters</li>
	<li>Multi-meter</li>
	<li>Small flat head screw driver</li>
</ul>

<p>...and if you make a soldering mistake you will need:</p>
<ul>
	<li>Desoldering pump or braid</li>
	<li>Long nose pliers</li> 
</ul>

<h2><a name="materials">Additional materials</a></h2>

<ul>
	<li>Medium sized (1mm) solder</li>
	<li>Insulation tape</li>
</ul>


