<h2>The electrodes</h2>
<table width="100%">
<tr>
	<td colspan="3" align="center">
		<img src="images/P2021.JPG" alt="Coax plug">
	</td>
</tr>
<tr>
<td>
<img class="imagepadded" src="images/coax_connects.jpeg" alt="Coax wire connectors" align="left">
<img class="imagepadded" src="images/earplug.jpeg" alt="Sponge ear plug" align="right">
<p>
The electrodes are made from coax connectors and ear plugs (sponge).
</p>
<p>
The pin assembly inside the plug is removable and is clamped onto the end of the cable with a screw. 
</p>
<p>The pin is inserted into a hole in the back of the sponge. You can use something like a bamboo skewer to prepare the hole.
</p>
<p>
The plug is then reassembled <em>through</em> the head band (<a href="#electrodes">see picture at top of page</a>).
</p>
<p>
The sponge is pre-soaked for at least a day in a saline solution.
</p>

<p>I also use one of these for my DRL (driven right leg).</p>
</p>
</td>
</tr>
</table>

<h2>The cabling</h2>
<table width="100%">
<tr>
<td>
<img class="imagepadded" src="images/5pindin.jpeg" alt="5 pin din male plug" align="left">
<p>
Initially I considered using audio jacks to connect my cables to the eeg but decided against it because they are easy to snap (especially when somebody walks away while still wearing the electrodes).<br>
Instead I opted to use a 5 pin din connector.
</p>
<p>I split the cable progressively down from 5 pair to a single core for each electrode. I was careful to continue the shielding and used heat shrink and tape to insulate the soldered joins.
</p>
</td>
</tr>
</table>

<h2>The head band</h2>
<table>
<tr>
<td>
<a href="images/headband.jpeg"><img class="imagepadded" src="images/headband_small.jpeg" alt="head band" align="left"></a>
<p>
The head band is a wide black plastic ladies hair band purchased from a chemist.
<p>
<p>
I did not own a drill bit large enough to fit the coax plugs so I used the biggest bit on hand and filed the rest out with a circular rasp.
</p>

<p>
It is important to remember that the head band rests a good 3cm (1/2 inch) away from the scalp when the electrodes are inserted. Take this into consideration when choosing where to drill your holes.
</p>
<p>I used the <a href="http://www.brainmaster.com/generalinfo/electrodeuse/eegbands/1020/1020.html">standard international 10/20 system</a> positions.
</p>
<p>
The driven right leg is placed in the middle of the scalp (CZ).</p>

</td>
</tr>
</table>

<h2>The saline solution</h2>
<p>
The saline is prepared by mixing a whole heap of table salt and water (I don't know how much is optimum, but more is better than less). It is best to use distilled water if you can. 
</p>
<p>
</p>

